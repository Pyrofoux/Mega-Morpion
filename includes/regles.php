                    <h2 class="margin text-center"> Qu'est-ce que le Méga Morpion ? </h2>
                    <br/>
                    <p> Le <strong>Méga Morpion</strong> est un jeu pour 2 joueurs qui se joue tour à tour. L’objectif pour gagner est de créer un alignement de 3 symboles. Cet alignement peut être vertical, horizontal ou diagonal.
                        <br/> Dans un morpion, les joueurs doivent aligner 3 croix ou 3 ronds pour gagner le morpion. Dans le Méga Morpion, il y a 9 petits morpions, formant un super morpion de 3x3. 
                        Pour gagner le jeu, <strong>il faut aligner dans ce méga morpion 3 petits morpions gagnés</strong> (en ligne, colonne ou diagonale). Un exemple de victoire est visible dans l’image ci-dessous, les 3 morpions du haut sont gagnés par le joueur croix (X).
                        <br/>
                        <br/>
                    </p>
                    <p class="image">
                        <img src="./images/image1.jpg" alt="image 1" class="image"/>
                    <br/>
                    <br/>
                    </p>
                    <p> <strong>La difficulté ? </strong> Les joueurs contrôlent dans quel morpion l’autre doit jouer en fonction de leurs derniers coups joués. 
                    Au départ le premier joueur doit commencer dans le morpion central, puis le suivant, doit jouer dans le morpion correspondant à la case jouée par le premier joueur.
                    Le petit morpion représente le super morpion. 
                    <br/>Par exemple, si le 1er joueur <strong>X</strong> vient de jouer dans la case en bas à droite, le 2ème joueur <strong>O</strong> doit jouer dans le morpion en bas à droite. 
                    Si ce dernier met un rond <strong>O</strong> dans la case en haut-milieu (du morpion en bas à droite), le joueur <strong>X</strong> doit alors jouer dans le morpion en haut-milieu.
                    <br/>
                    <br/>
                    </p>
                    <p> <strong>Lorsqu’un petit morpion est gagné,</strong> un « gros » rond ou croix est affiché en transparence au dessus du petit morpion, pour indiquer qu’il a été gagné par le joueur correspondant. 
                        S’il y a encore des cases vides dedans, les joueurs peuvent encore envoyer leur adversaire dedans.
                    <br/>
                    <br/>
                    </p>
                    <p class="image">
                        <img src="./images/image2.jpg" alt="image 2" class="image"/>
                    <br/>
                    </p>
                    <p class="legende"> 
                    <strong>1 -</strong> (X) joue dans la case en bas à droite, joueur (O) doit jouer dans le morpion en bas à droite.
                    <br/>
                    <strong>2 - </strong>(O) joue en haut-milieu, (X) doit jouer dans le morpion en haut milieu
                    <br/>
                    <br/>
                    </p>
                    <strong>Si un petit morpion est plein </strong>(plus de case vide) et qu’un joueur est quand même envoyé dedans, celui-ci peut alors jouer où il le souhaite dans le plateau.
