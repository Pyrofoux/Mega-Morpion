                    <h2 class="margin text-center"> Qu'est-ce que MEGA MORPION ? </h2>
                    <br/>
                    <p> <strong>MEGA MORPION</strong> est un site web conçu pour pouvoir joueur au Méga Morpion, jeu pour 2 joueurs qui se joue tour à tour. L’objectif pour gagner est de créer un alignement de 3 symboles. Cet alignement peut être vertical, horizontal ou diagonal.
                        <br/> Dans un morpion, les joueurs doivent aligner 3 croix ou 3 ronds pour gagner le morpion. Ici, dans le Méga Morpion, il y a 9 petits morpions, formant un super morpion de 3x3. 
                        <br/>
                        <br/>
                        Ce site vous permet, en plus d'affronter des joueurs en ligne, de pouvoir suivre votre progression grâce aux informations et statistiques contenues sur votre profil.
                        <br/>
                        Vous pouvez de plus voir dans le Classement votre niveau par rapport aux autres joueurs. 
                        <br/>
                        C'est également par cette page que vous pourrez accéder aux profils des autres joueurs et ainsi voir leurs statistiques.
                    <br/>
                    <br/>
                        Si vous êtes débutant, n'hésitez pas à consulter la page Règles du jeu qui vous expliquera les règles du Méga Morpion. 
                    </p>